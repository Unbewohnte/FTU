package addr

import (
	"net"
)

// Get local IP address; from https://stackoverflow.com/a/37382208
func GetLocal() (string, error) {
	conn, err := net.Dial("udp", "8.8.8.8:80")
	if err != nil {
		return "", err
	}
	defer conn.Close()

	localAddr := conn.LocalAddr().(*net.UDPAddr)

	return localAddr.IP.String(), nil
}
